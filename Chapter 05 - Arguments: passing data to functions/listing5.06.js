/* Get Programming with JavaScript
 * Listing 5.06
 * A function with two arguments
 */

var showSum;

showSum = function (number1, number2) {
	var total = number1 + number2;
	console.log("The sum is " + total);
};

showSum(30, 23);
showSum(2.8, -5);



